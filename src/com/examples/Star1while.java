// Right Angle Star Pattern//
public class Star1while{

	public static void main(String[] args)
	{
		int i =1;
		
		while(i<=5){
           int j=1;
			while(j<=i)
			{
			System.out.print("*");
			j++;
			}
		
		System.out.println( );
		i++;
		}
		//i=1 j=1
		//j=2 i=2
		//j=3 i=3




	}
}









		